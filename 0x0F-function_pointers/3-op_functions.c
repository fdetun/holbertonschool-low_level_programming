#include "3-calc.h"
/**
 * op_add - op_add a and b
 * @a: n1
 * @b: n2
 *
 * Return: op of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - op_sub a and b
 * @a: n1
 * @b: n2
 *
 * Return: op of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - op_mul a and b
 * @a: n1
 * @b: n2
 *
 * Return: op of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - op_div a and b
 * @a: n1
 * @b: n2
 *
 * Return: op of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - op_mod a and b
 * @a: n1
 * @b: n2
 *
 * Return: op of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
